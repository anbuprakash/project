package conceptloop;

public class ExampleLoop {

	public static void main(String[] args) {
			    //loop means again again iterative given values
	
	for(int times=5;times<10;times++) {  //( for(initialization;condition;increment/decrement))

		System.out.println("i am very happy");
	}
	
	int iterate=0;
	while(iterate<5) {  //while(condition) /n print stmt /n incre or decre
		System.out.println("i am feeling good");
		iterate++;
	}
	
	int again=0;
	do {
		System.out.println("very super");
		again++;
	} while (again<4);
	
//      return 0;
       
	}

}
