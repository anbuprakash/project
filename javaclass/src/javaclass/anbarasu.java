package javaclass;

public class anbarasu 
{
	int num1;
	int num2;
 int result;
	
	public void adding()
	{
		result=num1+num2;
	
	}

	public static void main(String[] args)
	{
		anbarasu prakash=new anbarasu();
		
		prakash.num1=12;
		prakash.num2=12;
		prakash.adding();
		System.out.println(prakash.result);

	}

}
